/// <summary>
/// The Document Spectrum (Docstrum) algorithm is a bottom-up page segmentation technique based on nearest-neighbourhood
/// clustering of connected components extracted from the document.
/// This implementation leverages bounding boxes and does not exactly replicate the original algorithm.
/// <para>See 'The document spectrum for page layout analysis.' by L. O'Gorman.</para>
/// </summary>
public sealed class CalyDocstrum
{
    private readonly CalyDocstrumOptions options;

    /// <summary>
    /// Create an instance of Docstrum for bounding boxes page segmenter, <see cref="CalyDocstrum"/>.
    /// </summary>
    public static readonly CalyDocstrum Instance = new CalyDocstrum();

    /// <summary>
    /// Create an instance of Docstrum for bounding boxes page segmenter using default options values.
    /// </summary>
    public CalyDocstrum() : this(new CalyDocstrumOptions())
    {
    }

    /// <summary>
    /// Create an instance of Docstrum for bounding boxes page segmenter using options values.
    /// </summary>
    /// <param name="options">The <see cref="CalyDocstrumOptions"/> to use.</param>
    /// <exception cref="ArgumentException"></exception>
    public CalyDocstrum(CalyDocstrumOptions options)
    {
        this.options = options ?? throw new ArgumentNullException(nameof(options));
    }

    /// <summary>
    /// Get the blocks.
    /// </summary>
    /// /// <param name="words">The page's words to segment into <see cref="PdfTextBlock"/>s.</param>
    /// <param name="cancellationToken"></param>
    /// <returns>The <see cref="PdfTextBlock"/>s generated by the document spectrum method.</returns>
    public IReadOnlyList<PdfTextBlock> GetBlocks(IEnumerable<PdfWord> words, CancellationToken cancellationToken)
    {
        cancellationToken.ThrowIfCancellationRequested();
        
        if (words is null)
        {
            return Array.Empty<PdfTextBlock>();
        }

        PdfWord[]? array = words?.Where(w => !w.Value.AsSpan().IsEmpty).ToArray();

        if (array is null || array.Length == 0)
        {
            return [];
        }

        var parallelOptions = new ParallelOptions()
        {
            MaxDegreeOfParallelism = options.MaxDegreeOfParallelism,
            CancellationToken = cancellationToken
        };

        return GetBlocks(array,
            options.WithinLineBounds, options.WithinLineMultiplier, options.WithinLineBinSize,
            options.BetweenLineBounds, options.BetweenLineMultiplier, options.BetweenLineBinSize,
            options.AngularDifferenceBounds,
            options.Epsilon,
            parallelOptions);
    }

    /// <summary>
    /// Get the blocks. See original paper for more information.
    /// </summary>
    /// <param name="words">The words to segment into <see cref="PdfTextBlock"/>s.</param>
    /// <param name="wlBounds">Angle bounds for words to be considered as neighbours on the same line.</param>
    /// <param name="wlMultiplier">Multiplier that gives the maximum euclidian distance between words for building lines.
    /// Maximum distance will be this number times the within-line distance found by the analysis.</param>
    /// <param name="wlBinSize">The bin size used when building the within-line distances distribution.</param>
    /// <param name="blBounds">Angle bounds for words to be considered as neighbours on separate lines.</param>
    /// <param name="blMultiplier">Multiplier that gives the maximum perpendicular distance between
    /// text lines for blocking. Maximum distance will be this number times the between-line
    /// distance found by the analysis.</param>
    /// <param name="blBinSize">The bin size used when building the between-line distances distribution.</param>
    /// <param name="angularDifferenceBounds">The angular difference bounds between two lines to be considered in the same block. This defines if two lines are parallel enough.</param>
    /// <param name="epsilon">Precision when testing equalities.</param>
    /// <param name="parallelOptions">A <see cref="ParallelOptions">ParallelOptions</see>
    /// instance that configures the behavior of this operation.</param>/// If it is -1, there is no limit on the number of concurrently running operations.</para></param>
    /// <returns>The <see cref="PdfTextBlock"/>s generated by the document spectrum method.</returns>
    private static IReadOnlyList<PdfTextBlock> GetBlocks(IReadOnlyList<PdfWord> words,
        AngleBounds wlBounds, float wlMultiplier, int wlBinSize,
        AngleBounds blBounds, float blMultiplier, int blBinSize,
        AngleBounds angularDifferenceBounds,
        float epsilon,
        ParallelOptions parallelOptions)
    {
        parallelOptions.CancellationToken.ThrowIfCancellationRequested();

        // 1. Estimate within line and between line spacing
        if (!GetSpacingEstimation(words, wlBounds, wlBinSize, blBounds, blBinSize,
            parallelOptions, out float withinLineDistance, out float betweenLineDistance))
        {
            if (float.IsNaN(withinLineDistance))
            {
                withinLineDistance = 0;
            }

            if (float.IsNaN(betweenLineDistance))
            {
                betweenLineDistance = 0;
            }
        }

        // 2. Determination of Text Lines
        float maxWithinLineDistance = wlMultiplier * withinLineDistance;
        var lines = GetLines(words, maxWithinLineDistance, wlBounds, parallelOptions).ToArray();

        // 3. Structural Block Determination
        float maxBetweenLineDistance = blMultiplier * betweenLineDistance;
        return GetStructuralBlocks(lines, maxBetweenLineDistance, angularDifferenceBounds, epsilon, parallelOptions).ToArray();
    }

    #region Spacing Estimation
    /// <summary>
    /// Estimation of within-line and between-line spacing.
    /// <para>This is the Docstrum algorithm's 1st step.</para>
    /// </summary>
    /// <param name="words">The list of words.</param>
    /// <param name="wlBounds">Angle bounds for words to be considered as neighbours on the same line.</param>
    /// <param name="wlBinSize">The bin size used when building the within-line distances distribution.</param>
    /// <param name="blBounds">Angle bounds for words to be considered as neighbours on separate lines.</param>
    /// <param name="blBinSize">The bin size used when building the between-line distances distribution.</param>
    /// <param name="parallelOptions">A <see cref="ParallelOptions">ParallelOptions</see>
    /// instance that configures the behavior of this operation.</param>
    /// <param name="withinLineDistance">The estimated within-line distance. Computed as the average peak value of distribution.</param>
    /// <param name="betweenLineDistance">The estimated between-line distance. Computed as the average peak value of distribution.</param>
    /// <returns>False if either 'withinLineDistance' or 'betweenLineDistance' is <see cref="float.NaN"/>.</returns>
    public static bool GetSpacingEstimation(IReadOnlyList<PdfWord> words,
        AngleBounds wlBounds, int wlBinSize,
        AngleBounds blBounds, int blBinSize,
        ParallelOptions parallelOptions,
        out float withinLineDistance, out float betweenLineDistance)
    {
        var withinLineDistList = new List<float>();
        var betweenLineDistList = new List<float>();

        // 1. Estimate within line and between line spacing
        CalyKdTree<PdfWord> kdTreeBottomLeft = new CalyKdTree<PdfWord>(words, w => w.BoundingBox.BottomLeft);

        Parallel.For(0, words.Count, parallelOptions,
            () => (wl: new List<float>(), bl: new List<float>()),
            (i, _, local) =>
            {
                var word = words[i];

                // Within-line distance
                // 1.1.1 Find the 2 closest neighbours words to the candidate, using euclidean distance.
                foreach (var n in kdTreeBottomLeft.FindNearestNeighbours(word, 2, w => w.BoundingBox.BottomRight, CalyDistances.Euclidean))
                {
                    // 1.1.2 Check if the neighbour word is within the angle of the candidate
                    if (wlBounds.Contains(AngleWL(word, n.Item1)))
                    {
                        local.wl.Add(CalyDistances.Euclidean(word.BoundingBox.BottomRight, n.Item1.BoundingBox.BottomLeft));
                    }
                }

                // Between-line distance
                // 1.2.1 Find the 2 closest neighbours words to the candidate, using euclidean distance.
                foreach (var n in kdTreeBottomLeft.FindNearestNeighbours(word, 2, w => w.BoundingBox.TopLeft, CalyDistances.Euclidean))
                {
                    // 1.2.2 Check if the candidate words is within the angle
                    var angle = AngleBL(word, n.Item1);
                    if (blBounds.Contains(angle))
                    {
                        // 1.2.3 Compute the vertical (between-line) distance between the candidate
                        // and the neighbour and add it to the between-line distances list
                        float hypotenuse = CalyDistances.Euclidean(word.BoundingBox.Centroid, n.Item1.BoundingBox.Centroid);

                        // Angle is kept within [-90, 90]
                        if (angle > 90)
                        {
                            angle -= 180;
                        }

                        var dist = MathF.Abs(hypotenuse * MathF.Cos((90 - angle) * MathF.PI / 180))
                                   - (float)word.BoundingBox.Height / 2.0f - (float)n.Item1.BoundingBox.Height / 2.0f;

                        // The perpendicular distance can be negative because of the subtractions.
                        // Could occur when words are overlapping, we ignore that.
                        if (dist >= 0)
                        {
                            local.bl.Add(dist);
                        }
                    }
                }

                return local;
            },
            local =>
            {
                lock (withinLineDistList)
                {
                    withinLineDistList.AddRange(local.wl);
                }
                lock (betweenLineDistList)
                {
                    betweenLineDistList.AddRange(local.bl);
                }
            });

        // Compute average peak value of distribution
        float? withinLinePeak = GetPeakAverageDistance(withinLineDistList, wlBinSize);
        float? betweenLinePeak = GetPeakAverageDistance(betweenLineDistList, blBinSize);

        withinLineDistance = withinLinePeak ?? float.NaN;
        betweenLineDistance = betweenLinePeak ?? float.NaN;

        return withinLinePeak.HasValue && betweenLinePeak.HasValue;
    }

    /// <summary>
    /// Get the average distance value of the peak bucket of the histogram.
    /// </summary>
    /// <param name="distances">The set of distances to average.</param>
    /// <param name="binLength"></param>
    private static float? GetPeakAverageDistance(List<float> distances, int binLength = 1)
    {
        if (distances.Count == 0)
        {
            return null;
        }

        if (binLength <= 0)
        {
            throw new ArgumentException("DocstrumBoundingBoxes: the bin length must be positive when computing peak average distance.", nameof(binLength));
        }

        float maxDbl = distances[0];
        for (int i = 1; i < distances.Count; i++)
        {
            if (distances[i] > maxDbl)
            {
                maxDbl = distances[i];
            }
        }

        maxDbl = MathF.Ceiling(maxDbl);
        if (maxDbl > int.MaxValue)
        {
            throw new OverflowException($"Error while casting maximum distance of {maxDbl} to integer.");
        }

        int max = (int)maxDbl;
        if (max == 0)
        {
            max = binLength;
        }
        else
        {
            binLength = binLength > max ? max : binLength;
        }

        int binCount = (int)Math.Ceiling(max / (double)binLength) + 1;
        var bins = new List<float>[binCount];
        for (int i = 0; i < binCount; i++)
        {
            bins[i] = new List<float>();
        }

        for (int i = 0; i < distances.Count; i++)
        {
            var distance = distances[i];
            int bin = (int)Math.Floor(distance / binLength);
            if (bin < 0)
            {
                throw new ArgumentOutOfRangeException(nameof(bin), "DocstrumBoundingBoxes: Negative distance found while commputing peak average distance.");
            }
            if (bin >= binCount)
            {
                bin = binCount - 1;
            }
            bins[bin].Add(distance);
        }

        List<float> best = null;
        for (int i = 0; i < binCount; i++)
        {
            var bin = bins[i];
            if (best is null || bin.Count > best.Count)
            {
                best = bin;
            }
        }

        if (best == null || best.Count == 0)
        {
            return null;
        }

        float sum = 0;
        for (int i = 0; i < best.Count; i++)
        {
            sum += best[i];
        }
        return sum / best.Count;
    }
