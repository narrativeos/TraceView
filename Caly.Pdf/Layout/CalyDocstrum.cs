    #region Text Lines
    /// <summary>
    /// Get the <see cref="PdfTextLine"/>s by grouping words using nearest neighbours.
    /// <para>This is the Docstrum algorithm's 2nd step.</para>
    /// </summary>
    /// <param name="words">The words to segment into <see cref="PdfTextLine"/>s.</param>
    /// <param name="maxWLDistance">The maximum within-line distance. Computed as the estimated within-line spacing times the within-line multiplier in the default implementation.</param>
    /// <param name="wlBounds">Angle bounds for words to be considered as neighbours on the same line.</param>
    /// <param name="parallelOptions">A <see cref="ParallelOptions">ParallelOptions</see>
    /// instance that configures the behavior of this operation.</param>
    /// <returns>The <see cref="PdfTextLine"/>s built.</returns>
    public static IEnumerable<PdfTextLine> GetLines(IReadOnlyList<PdfWord> words, float maxWLDistance, AngleBounds wlBounds, ParallelOptions parallelOptions)
    {
        var groupedWords = CalyClustering.NearestNeighbours(words,
            2,
            CalyDistances.Euclidean,
            (_, __) => maxWLDistance,
            pivot => pivot.BoundingBox.BottomRight,
            candidate => candidate.BoundingBox.BottomLeft,
            _ => true,
            (pivot, candidate) => wlBounds.Contains(AngleWL(pivot, candidate)),
            parallelOptions);

        foreach (var gr in groupedWords)
        {
            yield return new PdfTextLine(gr.OrderByReadingOrder().ToArray());
        }
    }

    /// <summary>
    /// Helper function to compute the within line angle between the pivot's bottom
    /// right and the candidate's bottom left points, taking in account the pivot's rotation.
    /// <para>-90 ≤ θ ≤ 90.</para>
    /// </summary>
    private static float AngleWL(PdfWord pivot, PdfWord candidate)
    {
        PdfPoint pivotBottomRight = pivot.BoundingBox.BottomRight;
        PdfPoint candidateBottomLeft = candidate.BoundingBox.BottomLeft;

        if (pivotBottomRight.Equals(candidateBottomLeft))
        {
            return 0;
        }

        float angle = CalyDistances.BoundAngle180(CalyDistances.Angle(in pivotBottomRight, in candidateBottomLeft) - (float)pivot.BoundingBox.Rotation);

        // Angle is kept within [-90;90] degree to handle overlapping words
        if (angle > 90)
        {
            angle -= 180;
        }
        else if (angle < -90)
        {
            angle += 180;
        }

        return angle;
    }
